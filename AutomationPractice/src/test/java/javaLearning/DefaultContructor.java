package javaLearning;

public class DefaultContructor {
int count;

	public static void main(String[] args) {
		// Example for default constructor
		// When we provide on constructor, compiler will provide one. It will provide value to the variables based on its return type
		
DefaultContructor contructor=new DefaultContructor();
System.out.println(contructor.count);
		
	}

}
